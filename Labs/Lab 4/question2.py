# Q2: List Slicing
# • Create a list named fruits containing "apple", "banana", "cherry", "date", "elderberry".
fruits = ["apple", "banana", "cherry", "date", "elderberry"]
# • Print the first three elements of the list.
print(f"first 3 elements: ", fruits[:3])
# • Print the last two elements of the list.
print(f"last 2 elements: ", fruits[-2:])
# • Print the elements from index 1 to index 3 (inclusive).
print(f"elements from index 1 to 3: ", fruits[1:4])